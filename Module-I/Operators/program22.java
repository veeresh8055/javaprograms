public class program22 {

    public static void main(String[] args) {
        // Unary operator
        int a = 10;
        System.out.println(a++);
        System.out.println(a);

        int b = 5;
        System.out.println(b--);
        System.out.println(b);


        int c = 17;
        System.out.println(c--);
        System.out.println(c--);
        System.out.println(c);
    }

}
//output
// 10
// 11

// 5
// 4
// 17
// 16
// 15
