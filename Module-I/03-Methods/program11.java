public class program11{
    public static void main(String[] args) {
        System.out.println(" Square of 1 : " + Square(1));
        System.out.println(" Square of 2 : " + Square(2));
        System.out.println(" Square of 3 : " + Square(3));
        System.out.println(" Square of 4 : " + Square(4));
        System.out.println(" Square of 5 : " + Square(5));
    }

    static int Square(int n ){
        return n*n;
    }
}
// output
//  Square of 1 : 1
//  Square of 2 : 4
//  Square of 3 : 9
//  Square of 4 : 16
//  Square of 5 : 25

