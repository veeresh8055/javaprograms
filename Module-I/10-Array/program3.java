
public class program3 {

    public static void main(String[] args) {
        // printing the array elem using for loop

        int[] arr = { 9, 8, 6, 5, 2, 6, 1, 1, 4, 4 };

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
// output
// 9
// 8
// 6
// 5
// 2
// 6
// 1
// 1
// 4
// 4