
public class problem3 {
    public static void main(String[] args) {

        // reverse array elements
        int[] arr = { 23, 98, 54, 17, 44, 65, 87 };

        for (int i = arr.length - 1; i >= 0; i--) {
            System.out.println(arr[i]);

        }
    }
}

// output :
// 87
// 65
// 44
// 17
// 54
// 98
// 23
