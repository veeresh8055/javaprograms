
public class program5 {
   public static void main(String[] args) {
     char choice = 'a';
     switch (choice) {
        case 'A':
            System.out.println("Apple");
            break;
        case 'B':
            System.out.println("Ball");
            break;
        case 'C':
            System.out.println("Cat");
            break;
        case 'D':
            System.out.println("Dog");
            break;
     
        default:
            System.out.println("Good Choice |*|");
            break;
     }
   }   
}
// output :
// Good Choice |*|
